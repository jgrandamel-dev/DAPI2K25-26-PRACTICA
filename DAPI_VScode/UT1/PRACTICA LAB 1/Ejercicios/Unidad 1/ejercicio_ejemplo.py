#Ejemplo: Implementa un programa que pida al usuario el nuúmero de horas trabajadas a la semana y el coste por hora.
# Realiza un programa que calcule la paga total y devuelva el valor en pantalla.


numero_horas_trabajadas = int(input("Introduce el numero de horas trabajadas: "))
coste_hora = int(input("Introduce el coste por hora: "))
paga = numero_horas_trabajadas * coste_hora
print ("la paga que le corresponde, será de ",paga, "€")

